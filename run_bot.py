"""
Модуль точки входа для торгового бота криптовалют.

Этот модуль служит основной точкой входа в приложение торгового бота. Он импортирует
асинхронную функцию main из модуля src.trading_bot и запускает её с помощью
asyncio.run(), если скрипт выполняется напрямую (т.е. как __main__).

Импорты:
- asyncio: Для работы с асинхронными задачами.
- logging: Для логирования (хотя не используется напрямую, может быть настроен в main).
- os: Для работы с переменными окружения (хотя не используется напрямую, может быть
  полезен в main).

Логика: При запуске проверяется __name__ == "__main__",
и если да, то запускается main() торгового бота.

Обработка ошибок: Любые исключения, возникающие в main(), будут подняты и отображены
в консоли, так как asyncio.run() не обрабатывает их автоматически.
"""

import asyncio
import sys

from config import Config
from src.trading_bot import main

if __name__ == "__main__":
    try:
        Config().validate()
    except ValueError as exc:
        print(f"[CONFIG ERROR] {exc}", file=sys.stderr)
        sys.exit(1)

    asyncio.run(main())
